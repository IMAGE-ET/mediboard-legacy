require_once("planning.class.php");

$obj = new Cplanning();
$msg = '';

if (!$obj->bind( $_POST )) {
	$AppUI->setMsg( $obj->getError(), UI_MSG_ERROR );
	$AppUI->redirect();
}

$del = dPgetParam( $_POST, 'del', 0 );

if ($del) {
	if (!$obj->canDelete( $msg )) {
		$AppUI->setMsg( $msg, UI_MSG_ERROR );
		$AppUI->redirect();
	}

	if (($msg = $obj->delete())) {
		$AppUI->setMsg( $msg, UI_MSG_ERROR );
		$AppUI->redirect();
	} else {
		$AppUI->setMsg( "Admission supprime", UI_MSG_ALERT);
		$AppUI->redirect( "m=dPplanningOp" );
	}
} else {
	if (($msg = $obj->store())) {
		$AppUI->setMsg( $msg, UI_MSG_ERROR );
	} else {
		$isNotNew = @$_POST['operation_id'];
		$AppUI->setMsg( $isNotNew ? 'Admission modifie' : 'Admission cre', UI_MSG_OK);
	}
	$AppUI->redirect();
}
?>